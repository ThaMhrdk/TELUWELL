            private void populateProdiComboBox() {
                try {
                    ArrayList<Prodi> daftarProdi = dbMahasiswa.getAllProdi();
                    DefaultComboBoxModel<Prodi> modelProdi = new DefaultComboBoxModel<>();

                    for (Prodi prodi : daftarProdi) {
                        modelProdi.addElement(prodi);
                    }
                    jComboBoxProdi.setModel(modelProdi);

                    jComboBoxProdi.setRenderer(new javax.swing.DefaultListCellRenderer() {
                        @Override
                        public java.awt.Component getListCellRendererComponent(
                                javax.swing.JList<?> list, Object value, int index,
                                boolean isSelected, boolean cellHasFocus) {
                            super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);
                            if (value instanceof Prodi) {
                                Prodi prodi = (Prodi) value;
                                setText(prodi.getNmprodi());
                            }
                            return this;
                        }
                    });

                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(this, 
                        "Error memuat daftar prodi: " + e.getMessage(), 
                        "Error Database", JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
        }
}

private boolean validateInputs() {
    if (jTextFieldNIM.getText().isEmpty() ||    
        jTextFieldEmail.getText().isEmpty() ||
        jTextFieldNamaDepan.getText().isEmpty() ||
        jTextFieldNamaBelakang.getText().isEmpty() ||
        jComboBoxJenisKelamin.getSelectedItem() == null ||
        jComboBoxProdi.getSelectedItem() == null ||
        jPasswordFieldPassword.getPassword().length == 0) {

        JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
        return false;
    }
    return true;
}


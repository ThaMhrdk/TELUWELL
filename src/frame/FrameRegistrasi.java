    private void jButtonDaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDaftarActionPerformed

        if (validateInputs()) {
            try {
                String nim = jTextFieldNIM.getText();
                String email = jTextFieldEmail.getText();
                String nmdepan = jTextFieldNamaDepan.getText();
                String nmbelakang = jTextFieldNamaBelakang.getText();
                String jk = jComboBoxJenisKelamin.getSelectedItem().toString();

                Prodi selectedProdi = (Prodi) jComboBoxProdi.getSelectedItem();
                String idprodi = selectedProdi.getIdprodi();

                String password = new String(jPasswordFieldPassword.getPassword());

                Mahasiswa mahasiswaBaru = new Mahasiswa(nim, email, nmdepan, nmbelakang, jk, idprodi, password);

                boolean success = dbMahasiswa.insertMahasiswa(mahasiswaBaru);

                if (success) {
                    JOptionPane.showMessageDialog(this, "Registrasi Mahasiswa Berhasil!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                    clearForm();
                    FrameLoginMahasiswa frameLogin = new FrameLoginMahasiswa();
                    frameLogin.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Registrasi Mahasiswa Gagal. NIM mungkin sudah terdaftar.", "Gagal", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException e) {
                if (e.getMessage().contains("Duplicate entry") && e.getMessage().contains("for key 'PRIMARY'")) {
                    JOptionPane.showMessageDialog(this, "NIM yang Anda masukkan sudah terdaftar. Gunakan NIM lain.", "Registrasi Gagal", JOptionPane.ERROR_MESSAGE);
                } else if (e.getMessage().contains("Duplicate entry") && e.getMessage().contains("for key 'email'")) {
                    JOptionPane.showMessageDialog(this, "Email yang Anda masukkan sudah terdaftar. Gunakan email lain.", "Registrasi Gagal", JOptionPane.ERROR_MESSAGE);
                }
                else {
                    JOptionPane.showMessageDialog(this, "Terjadi kesalahan database: " + e.getMessage(), "Error Database", JOptionPane.ERROR_MESSAGE);
                }
                e.printStackTrace();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButtonDaftarActionPerformed

